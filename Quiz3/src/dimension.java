

public class dimension {
	private int alto;
	private int ancho;
	private int largo;
	

	public dimension(int palto, int pancho, int plargo) {
		this.alto = palto;
		this.ancho = pancho;
		this.largo = plargo;
	}

	public int getAlto() {
		return alto;
	}

	public int getAncho() {
		return ancho;
	}

	public int getLargo() {
		return largo;
	}
	
}
